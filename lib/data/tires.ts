export const getTireById = (id: string): Tire | undefined =>
  tires.find((t) => t.id === id);

export const getFeaturedTires = (): Tire[] =>
  tires.filter((t) => t.tags.includes("bestseller")).slice(0, 6);

export const getRelatedTires = (tire: Tire, count = 4): Tire[] =>
  tires
    .filter((t) => t.id !== tire.id && (t.category === tire.category || t.brand === tire.brand))
    .slice(0, count);

export const getAllBrands = (): string[] =>
  [...new Set(tires.map((t) => t.brand))].sort();

export const getAllSizes = () => ({
  widths: [...new Set(tires.map((t) => t.size.width))].sort((a, b) => a - b),
  aspectRatios: [...new Set(tires.map((t) => t.size.aspectRatio))].sort((a, b) => a - b),
  rimDiameters: [...new Set(tires.map((t) => t.size.rimDiameter))].sort((a, b) => a - b),
});
